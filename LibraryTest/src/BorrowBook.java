 class BorrowBook extends Book {

    private String studentID;
    private LocalDate borrowDate;

    public BorrowBook(String title, String author, String studentID, LocalDate borrowDate) {
        super(title, author);
        this.studentID = studentID;
        this.borrowDate = borrowDate;
    }

    public String getStudentID() {
        return studentID;
    }

    public void setStudentID(String studentID) {
        this.studentID = studentID;
    }

    public LocalDate getBorrowDate() {
        return borrowDate;
    }

    public void setBorrowDate(LocalDate borrowDate) {
        this.borrowDate = borrowDate;
    }

    public LocalDate calculateExpiryDate() {
        return LocalDate.now().plusDays(7); // Return date is today + 7 days
    }
}
